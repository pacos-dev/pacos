create table if not exists PUBLIC.APP_USER
(
    id                     INTEGER GENERATED BY DEFAULT AS SEQUENCE APP_USER_SEQ PRIMARY KEY not null,
    LOGIN                  VARCHAR(255) not null,
    PASSWORD_HASH          VARCHAR(255) not null,
    TOKEN VARCHAR
(
    255
)
    );
