CREATE SEQUENCE if not exists APP_DOCK_CONFIG_SEQ START WITH 1 INCREMENT BY 1;
create table if not exists PUBLIC.APP_DOCK_CONFIG
(
    ID        INTEGER GENERATED BY DEFAULT AS SEQUENCE APP_DOCK_CONFIG_SEQ PRIMARY KEY not null,
    ACTIVATOR VARCHAR(255),
    ORDER_NUM INTEGER not null,
    USER_ID   INTEGER not null,
    FOREIGN KEY (USER_ID) REFERENCES PUBLIC.APP_USER (id)
    ON DELETE CASCADE ON UPDATE CASCADE
    );

